import { type EmailOtpType } from '@supabase/supabase-js';
import { type NextRequest, NextResponse } from 'next/server';
import { createServerClient } from '@supabase/ssr';
import { cookies } from 'next/headers';

export async function GET(request: NextRequest) {
  const { searchParams } = new URL(request.url);
  const token_hash = searchParams.get('token_hash');
  const type = searchParams.get('type') as EmailOtpType | null;
  const next = '/moccet-mail/onboarding';
  const origin = request.nextUrl.origin;

  console.log('Confirm route received:', { token_hash: !!token_hash, type });

  if (token_hash && type) {
    const cookieStore = await cookies();

    const supabase = createServerClient(
      process.env.NEXT_PUBLIC_SUPABASE_URL!,
      process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY!,
      {
        cookies: {
          getAll() {
            return cookieStore.getAll();
          },
          setAll(cookiesToSet) {
            cookiesToSet.forEach(({ name, value, options }) =>
              cookieStore.set(name, value, options)
            );
          },
        },
      }
    );

    const { error } = await supabase.auth.verifyOtp({
      type,
      token_hash,
    });

    if (error) {
      console.error('Error verifying OTP:', error);
      return NextResponse.redirect(`${origin}/moccet-mail/auth?error=verification_failed`);
    }

    console.log('OTP verification successful');
    return NextResponse.redirect(`${origin}${next}`);
  }

  // If no token_hash, redirect to auth page
  return NextResponse.redirect(`${origin}/moccet-mail/auth?error=missing_token`);
}
